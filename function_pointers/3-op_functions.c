/**
 * function to add
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 *function to subtract
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 *function to mulitpy
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * function to divide
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 *function to get remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
